/*
Ejercicio 9:

Una milla son 1.6 kilometros.

¿Cuantos kilometros tiene una autopista de 120 millas?

*/

const milla_kilometros = 1.6;
let millas_autopista = 120;

let kilometrosAutopista = millas_autopista * milla_kilometros;

console.log(`La autopista de ${millas_autopista} millas tiene ${kilometrosAutopista} kilómetros.`);